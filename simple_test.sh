#! /bin/bash

################################################################################
# we download some banks
################################################################################
wget "ftp://ftp.sra.ebi.ac.uk/vol1/fastq/SRR387/SRR387476/SRR387476.fastq.gz"
gunzip SRR387476.fastq.gz

################################################################################
# we launch leon
################################################################################
./build/leon -c -lossless -file SRR387476.fastq
./build/leon -d -file SRR387476.fastq.leon

################################################################################
# we check the result
################################################################################

diff ./SRR387476.fastq ./SRR387476.fastq.d
if [ $? -eq 0 ]; then
   echo "TEST OK"
else
   echo "TEST KO"
fi

################################################################################
# clean up
################################################################################
rm -f  SRR387476.*

